public class Difuntos {
    char sexo;
    String edad;
    String fecha;
    String causa;
    String desc_causa;
    String permanencia;
    String nombre_sede;

    public Difuntos(char sexo, String edad, String fecha, String causa, String desc_causa, String permanencia, String nombre_sede) {
        this.sexo = sexo;
        this.edad = edad;
        this.fecha = fecha;
        this.causa = causa;
        this.desc_causa = desc_causa;
        this.permanencia = permanencia;
        this.nombre_sede = nombre_sede;
    }
    
    
}
